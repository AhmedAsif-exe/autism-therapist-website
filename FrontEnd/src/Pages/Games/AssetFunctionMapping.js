// Helper function to get all functions
export function getAllFunctions() {
  return Object.keys(FUNCTION_ASSET_MAPPINGS);
}

// Helper function to get assets for a specific function
export function getAssetsForFunction(functionName) {
  return FUNCTION_ASSET_MAPPINGS[functionName] || [];
}

// Helper function to get all unique assets across all functions
export function getAllAssets() {
  const allAssets = new Map();
  Object.values(FUNCTION_ASSET_MAPPINGS).forEach(assets => {
    assets.forEach(asset => {
      allAssets.set(asset.imagePath, asset);
    });
  });
  return Array.from(allAssets.values());
}

// Helper function to convert to the format expected by Game 2
export function convertToGame2Format() {
  return Object.entries(FUNCTION_ASSET_MAPPINGS).map(([fn, assets]) => ({
    fn,
    correct: assets.map(asset => ({ 
      name: asset.name, 
      image: asset.imagePath 
    }))
  }));
}
