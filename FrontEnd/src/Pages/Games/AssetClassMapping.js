// Helper function to get all classes
export function getAllClasses() {
  return Object.keys(CLASS_ASSET_MAPPINGS);
}

// Helper function to get assets for a specific class
export function getAssetsForClass(className) {
  return CLASS_ASSET_MAPPINGS[className] || [];
}

// Helper function to get all unique assets across all classes
export function getAllClassAssets() {
  const allAssets = new Map();
  Object.values(CLASS_ASSET_MAPPINGS).forEach(assets => {
    assets.forEach(asset => {
      allAssets.set(asset.imagePath, asset);
    });
  });
  return Array.from(allAssets.values());
}

// Helper function to convert to the format expected by Game 6
export function convertToGame6Format() {
  return Object.entries(CLASS_ASSET_MAPPINGS)
    .filter(([cls, assets]) => assets.length >= 3) // Only classes with 3+ items
    .map(([cls, assets]) => ({
      cls,
      items: assets.map(asset => asset.imagePath)
    }));
}

// Helper function to find what class(es) an asset belongs to
export function getClassesForAsset(imagePath) {
  const classes = [];
  Object.entries(CLASS_ASSET_MAPPINGS).forEach(([className, assets]) => {
    if (assets.some(asset => asset.imagePath === imagePath)) {
      classes.push(className);
    }
  });
  return classes;
}
