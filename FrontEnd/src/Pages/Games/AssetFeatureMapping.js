// Helper function to get all features
export function getAllFeatures() {
  return Object.keys(FEATURE_ASSET_MAPPINGS);
}

// Helper function to get assets for a specific feature
export function getAssetsForFeature(featureName) {
  return FEATURE_ASSET_MAPPINGS[featureName] || [];
}

// Helper function to get all unique assets across all features
export function getAllFeatureAssets() {
  const allAssets = new Map();
  Object.values(FEATURE_ASSET_MAPPINGS).forEach(assets => {
    assets.forEach(asset => {
      allAssets.set(asset.imagePath, asset);
    });
  });
  return Array.from(allAssets.values());
}

// Helper function to convert to the format expected by Game 3/4
export function convertToGame3Format() {
  return Object.entries(FEATURE_ASSET_MAPPINGS)
    .filter(([feature, assets]) => assets.length >= 3) // Only features with 3+ items
    .map(([feature, assets]) => ({
      feature,
      correct: assets.map(asset => asset.imagePath)
    }));
}

// Helper function to find what feature(s) an asset has
export function getFeaturesForAsset(imagePath) {
  const features = [];
  Object.entries(FEATURE_ASSET_MAPPINGS).forEach(([featureName, assets]) => {
    if (assets.some(asset => asset.imagePath === imagePath)) {
      features.push(featureName);
    }
  });
  return features;
}
